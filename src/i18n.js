export function createTranslator(language) {
  const lang = dictionaries[language] ? language : 'zh-CN';
  const dictionary = dictionaries[lang];

  return (key, params = {}) => {
    const template = dictionary[key] || dictionaries['zh-CN'][key] || key;
    return Object.entries(params).reduce((value, [paramKey, paramValue]) => (
      value.replaceAll(`{${paramKey}}`, String(paramValue))
    ), template);
  };
}

